package webproject.dto;

public class AccountDTO {
	private int Account_Num, Account_Flag;
	private String Account_Id, Account_Password, Account_Name, Account_Email, Account_Img, Account_Phon_Num, Account_About;

	public AccountDTO() {

	}

	public int getAccount_Num() {
		return Account_Num;
	}

	public void setAccount_Num(int account_Num) {
		Account_Num = account_Num;
	}

	public int getAccount_Flag() {
		return Account_Flag;
	}

	public void setAccount_Flag(int account_Flag) {
		Account_Flag = account_Flag;
	}

	public String getAccount_Id() {
		return Account_Id;
	}

	public void setAccount_Id(String account_Id) {
		Account_Id = account_Id;
	}

	public String getAccount_Password() {
		return Account_Password;
	}

	public void setAccount_Password(String account_Password) {
		Account_Password = account_Password;
	}

	public String getAccount_Name() {
		return Account_Name;
	}

	public void setAccount_Name(String account_Name) {
		Account_Name = account_Name;
	}

	public String getAccount_Email() {
		return Account_Email;
	}

	public void setAccount_Email(String account_Email) {
		Account_Email = account_Email;
	}

	public String getAccount_Img() {
		return Account_Img;
	}

	public void setAccount_Img(String account_Img) {
		Account_Img = account_Img;
	}

	public String getAccount_Phon_Num() {
		return Account_Phon_Num;
	}

	public void setAccount_Phon_Num(String account_Phon_Num) {
		Account_Phon_Num = account_Phon_Num;
	}
	
	public String getAccount_About() {
		return Account_About;
	}

	public void setAccount_About(String account_About) {
		Account_About = account_About;
	}

}
